package org.openmrs.module.deriveddata.api.db.hibernate;

import org.apache.commons.logging.Log;
import org.apache.commons.logging.LogFactory;
import org.hibernate.Criteria;
import org.hibernate.SessionFactory;
import org.hibernate.criterion.Expression;
import org.openmrs.OpenmrsObject;
import org.openmrs.module.deriveddata.api.db.DerivedDataDAO;

/**
 * It is a default implementation of  {@link DerivedDataDAO}.
 */
public class HibernateDerivedDataDAO implements DerivedDataDAO {
	protected final Log log = LogFactory.getLog(this.getClass());
	
	private SessionFactory sessionFactory;
	
	/**
     * @param sessionFactory the sessionFactory to set
     */
    public void setSessionFactory(final SessionFactory sessionFactory) {
	    this.sessionFactory = sessionFactory;
    }
    
	/**
     * @return the sessionFactory
     */
    public SessionFactory getSessionFactory() {
	    return sessionFactory;
    }

    @Override
    public OpenmrsObject getObjectByUuid(final String classname, final String uuid) {
        Criteria criteria = getSessionFactory().getCurrentSession().createCriteria(classname);
        criteria.add(Expression.eq("uuid", uuid));
        return (OpenmrsObject) criteria.uniqueResult();
    }
}